#include <stdio.h>

int main() {
    int num1 = 10;
    int num2 = 5;

    // Basic Operations
    num1 + num2; // 15
    num1 - num2; // 5
    num1 * num2; // 50
    num1 / num2; // 2

    // Modulus
    num1 % num2; // 0

    // Assignment
    num1 = num2; // 5
    num1 += num2; // 10
    num1 -= num2; // 5
    num1 *= num2; // 50
    num1 /= num2; // 10
    num1 %= num2; // 0

    // Increment and Decrement
    num1++; // 11
    num1--; // 9
}